	        		<p style="margin-top: 0; margin-bottom: 0;"><img src="images/customculinary2.jpg" alt="floor protection in powder food company" width="321" height="240" hspace="5" vspace="5" align="right">Custom Culinary creates fine quality, authentic bases, sauces and gravies for foodservice and food processing customers. Founded in 1945, the company became a pioneer creating dry beef and chicken soup concentrate.  In 1991, Griffith Laboratories purchased the Oswego operation and then in 2004, the changed its name to Custom Culinary, Inc. to better reflect the company’s focus on providing a full line of authentic culinary flavor solutions.</p>
					<p>The production schedule is critical to its success. The manufacturing space, starting at the 24 foot ceiling height, is fully utilized beginning at the mezzanine where ingredients are delivered to the mixers.  Because Custom Culinary creates various bases, sauces and gravies for numerous customers, changing the product mix is a frequent routine. This requires frequent wash downs utilizing high temperature water and cleaning chemicals for its sanitation procedures.</p>
					<p>This is where the problem begins. The sloping on the floor had been poorly done creating puddling and birdbaths. The constant standing water migrated into the porous floor topping and settled between the topping and the concrete. The trench built to collect water had also deteriorated.  Excess water overflowed into the aisleways. The flooring system was a great candidate for bacteria growth.</p>
					<br>
					<h2>Timing and Scheduling of Personnel</h2>
					<p>Working within a facility that specializes in mixing and creating powders poses special challenges. The production area required detailed preparation as the equipment could not be moved, and dust had to be contained. Concare began the preparation process on Friday afternoon after Custom Culinary removed all food items from the area. A stud wall with thick plastic that reached the top of the 24 ft. high ceiling was erected. Large mixers, conveyors and all process equipment were carefully wrapped in plastic protecting the production line from any dust particles. The preparation process took a full 8 hours before any work started.</p>
					<p>Concare utilized three crews working in shifts to prepare the facility, install the new floor, apply the top coat and remove all containment materials, all completed within a single weekend. All remnants of the old flooring system were removed from the concrete, placed into plastic bags and transported out of the work area. High powered vacuums were used to promptly remove dust and debris from the environment.</p>
					<p>There was an additional requirement driving the project: plant management needed not only a highly functional floor, but one that had an aesthetically professional appearance. Custom Culinary services many customers that visit the facility. It was critical to show off the production floor not only as a sanitary environment but one that would put customers at ease.</p>
					<p>Concare chose a cementitious urethane 3/8 inch thick that provided a high gloss finish, chemical protection and slip resistant. It is critical that the flooring solution endures high temperatures, meets USDA standards and can withstand the weight requirements of numerous forklifts working in the area.</p>
					<br>
					<h2>Quality Control</h2>
