	        		<h1>High Performance Floor Protection for Food Flavor and Ingredients Company</h1>
	        		<p style="margin-top: 0; margin-bottom: 0;"><img src="images/synergy1.jpg" alt="high traffic floor coating" width="321" height="240" hspace="5" vspace="5" align="right">Synergy Flavors is a leading international supplier of value-added flavors and ingredients to the global food and beverage industry. The 70,000 sq. ft. renovation had to be completed within 16 months, integrating their Chicago plant into the new Wauconda facility.</p>
					<p>Synergy used a general contractor for the renovation, but decided to go a different direction for their floors. Synergy evaluated the products used in previous years, and was introduced to Concare and their proven system for installation. Their designer and project manager experiences quickly identified Concare as an expert and awarded this portion of the project to them.</p>
					<br>
					<h2>The Evaluation - What is to Come</h2>
					<p>Concare evaluated what was on... and not on... the current concrete floors. Based upon the plans and the operations of the facility, Concare identified various flooring solutions based upon room requirements:</p>
					<li>Closed room for storing products that maintained a temperature over 100 degrees</li>
					<li>Closed rooms for dry powdered flavors</li>
					<li>Open area exposed to wet conditions</li>
					<li>Open area that needed to be chemically resistant to their products</li>
					<li>Heavy forklift traffic in all areas of the facility</li>
					
					<p>Flavor drums, totes, and the thousands of raw materials in the facility have the potential of getting on the floor. If that happens, it can quickly deteriorate the surface. </p>
					<p>Concare tested the most aggressive liquid materials on various flooring solutions and provided recommendations to withstand Synergy's varied conditions.</p>
					<br>
					<h2>Testing the Recommendation</h2>
					<p style="margin-top: 0; margin-bottom: 0;"><img src="images/synergy2.jpg" alt="integral cove eliminates corners of floors and improves sanitation on industrial floor" width="321" height="240" hspace="5" vspace="5" align="right">The product manufacturer also tested the preliminary flooring recommendations. These lab results indicated that when the flooring system experienced a spill, the area exposed could withstand an eight hour period before experiencing damage or staining.</p>
					<br>
					<h2>Preparation & Installation</h2>
					<p>Over the 16 month construction schedule, the Concare installation team was called in at various points during the last 6 months to complete the production areas.</p>
					<p>Area 1: The solution for the heaviest use areas consisted of a slurry-broadcast, chemical resistant, antimicrobial cementitious urethane system with an enhanced traction and abrasion resistant, high performance epoxy top coat.</p>
					<p>Area 2: The traffic aisles were treated with a abrasion resistant, enhanced traction high performance epoxy top coat where texture and appearance remains constant.</p>
					<p>Area 3: The utility area is a temporary working space that needed to be treated for dust control and cleanability. The solution installed was a water based, epoxy coating, durable for the type of tasks performed.</p>
					<br>
					<h2>The Sloping Affect</h2>
					<p>As the production lines were being installed, Synergy needed to be sure that the equipment was level since the concrete floor was sloped. The company placed the equipment legs on base plates. Concare suggested raising the base plates from the floor to allow grouting and sealing at the base of the legs. This allowed water and any other liquids to flow away from the production line and not settle under the base of the feet keeping the area clean and sanitary.</p>
					<p>Concare did not stop there...they also built a cove base along the walls, eliminating any right angles, for easy cleaning and sanitation. In addition, Concare installed berms as passive containment in the event water were to flow out of a room in the event of a spill or if an overspill occurs.</p>
					<br>
					<h2>Striping</h2>
					<p>Striping is an important part of safety in a facility. Concare installed striping into the new flooring system in order to identify traffic lanes for forklift and foot traffic.</p>
