	        	<h1>The Concare, Inc. Story</h1>
	        	<p>In the early 1970’s, Concare, Inc.© co-founder Ron Puszynski sold specialty cleaning and maintenance products for Brulin & Company, an Indianapolis-based firm that serviced major national chains. Brulin’s focus was on training their customer’s employees to use chemical products safely and effectively.</p>
				<p>Solvents and acids were commonly used for concrete prep in the ‘70s, and naturally their hazardous nature meant that Brulin’s concrete coating and repair products required both knowledgeable supervision and safe handling. Ron was frequently on site from a project’s start to its finish, making sure that the end result conformed to Brulin’s requirements and their customers’ expectations.</p>
				<p>Then an oil embargo and recession hit. Brulin saw their clients trimming staff and substituting outside contract services for full-time employees. Excess maintenance personnel were targeted and eliminated. A renewed focus on “core competencies” became the industry mantra, but the need for concrete remediation remained.</p>
				<p>Customers turned to their valued suppliers for help. Ron evaluated and tested outside contractors on his customer’s behalf but found that their attention to detail and commitment weren’t up to the standards of distinguished national companies. Knowing he could do better, Ron and his wife Chris founded Concare, Inc. ® as 50/50 partners in 1978, and its slogan, “We Treat Floors Right ®” was born.</p>
				<p><b>The Early Years</b></p>
				<p>Everyone knows the tale of Hewlett Packard, which got its start in a now-legendary two car garage. Concare, Inc. had its own humble beginnings in the basement of Ron and Chris’s home, with an area in the garage for storing equipment.</p>
				<p>Floor coatings in industrial, commercial and residential facilities were new technology in the late 70’s, and the demand for concrete remediation was much lower than it is today. As a result, Concare’s projects were typically scheduled over weekends: at first, there were just one or two jobs a month and Concare’s “crews” consisted of mostly family members and friends. On Ron’s watch, these folks could be counted on to complete simple projects and make a little mad money to boot.</p>
				<p>Slowly, Concare, Inc. and its reputation grew. Full-time employees signed on, some of whom are still working at Concare today, like Lisa (Accounting) and Stanley (Field Operations.) The basement was abandoned for a Franklin Park, IL industrial building as demand for Concare’s services expanded, and Chris finally got some quiet sleeping time when the business’s in-house nighttime activity ceased.</p>
				<p>Business boomed in the following years and Concare, Inc. needed still more space. The company relocated to a converted Melrose Park, IL warehouse in 2000 after extensive renovations. The new home base was 2.5 times the size of the Franklin Park location and featured a “working showroom” where customers could see examples of various flooring systems.</p>
				<p>Concare’s continual growth is the direct result of repeat customers who, after experiencing unprecedented professionalism, attention to detail and quality, have returned again and again. Even today, about 2/3 of Concare’s revenue comes from past customers and their referrals.</p>
				<p><b>Concare Today</b></p>
				<p>The company also remains on the forefront of technology with Concare’s Management Information System (CMIS), a unique management tool that tracks customer information, creates job estimates, executes purchase orders and provides custom data printouts.</p>
				<p>With its top-notch team, impeccable organization and outstanding reputation for quality, it’s no surprise that Concare, Inc. is continually recognized as an industry leader, as with ABC’S Excellence in Construction award.</p>
				<p>Experience, professionalism and commitment: three words that were the seeds of our business, and are the hallmarks for which Concare, Inc. ® is known today.</p>
