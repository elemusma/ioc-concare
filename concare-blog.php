					<p><b>What are the Signs of Moisture in Concrete?</b></p>
					<p>Flooring failures can occasionally be attributed to moisture in the concrete. If you are not sure if moisture is affecting your floor, here are a few signs that can be indicators.</p>
					<p><img src="images/wfc.JPG" alt="moisture mitigation" align="center"/></p>
					<p>Signs of moisture on bare concrete floors:</p>
					<p>•	Efflorescence. Efflorescence is a white powdery substance that appears on the surface of the floor or walls. the moisture makes its way up through the concrete and when it dries on the surface, it leaves a salt-like deposit behind. It can appear around crack or joints in the floor as they are a perfect exit point for moisture.</p>
					<p>•	Rubber Mat. A rubber mat, used at a workstation or an entryway, can be an indicator for moisture. If the rubber mat has been in that spot for more than 48 hours, lift it up and check if there is a dark discoloration left behind. If it's the same color as the bare concrete next to it, moisture may not be at elevated levels. If it's darker than the concrete around it, higher moisture may be present.</p>
					<p>Signs of moisture under vinyl and coated concrete floors.</p>
					<p>•	Bubbles, Air Pockets. Moisture can appear as bubbles, air pockets or even elongated strands 4 to 12 inches in length. These areas will appear lighter, or it may be cracked with a liquid discharge.</p>
					<p>Signs of moisture under carpet tile on the concrete floor:</p>
					<p>•	If the carpet tiles are loose, pick one up. If there's dampness underneath or if you notice that the adhesive is sticky, these are clear indications of moisture.</p>
