					<p>A: The original building was constructed in 1978 with the back part of the facility consisting of 3 bays which was the Public Works department and the front part of the building was the Police department. The shop area was approximately 4,000 sq ft and we had to store all of our cars and trucks outside. </p>
					<p>In 1986 we constructed our first addition so the vehicles could be stored indoors. At that time, we also moved the offices to the front part of the building and turned the offices into our parts storage area. In 1994-1995 we expanded again, this time increasing the shop size from 3 bays to 5 bays, which gave us now a total of over 9,000 sq ft. We more than doubled the size of the shop floor.</p>
