	        		<h1 itemprop="name" style="margin-bottom: 0">Quality Assurance System for Concrete Floor Coatings</h1>
                   <p style="margin-top: 0;">Over 35 years ago, Ron Puszynski, president of Concare, identified a 4 step process called the Quality Assurance System, which remains relevant today. Being both strategic and practical, the 
<span itemprop="description">Quality Assurance System is a series of structured activities that produces a specific result at the completion of each step</span>. The action of ‘painting on epoxy’ or ‘need to put on a coat of epoxy’ doesn’t work in the environments companies need to maintain.</p>
                   <p itemprop="brand" itemscope itemtype="https://schema.org/Brand" itemref="_logo6" style="margin-top: 0;">If you are leading a concrete floor coating project and you are not aware of the 
<span itemprop="name">Quality Assurance System</span>, here’s a summary of it now. When we come to see you personally, you’ll quickly find out why Concare is the best at delivering value at each of the 4 steps.</p>
				   <br/>
				   <img itemprop="image" src="images/QualityAssuranceGraphic.jpg" alt="quality assurance system concrete floor coating process" width="699.84" height="134.4" hspace="5" vspace="5" align="left"/>
				   <p style="margin-top: 0;">Step 1: Evaluation</p>
					<p style="margin-top: 0;">This process takes a look at all of the issues that need to be addressed. There are 6 criteria’s that we evaluate including moisture, chemical exposure, level of traffic, temperatures, surface coatings, and deterioration. We’ll also work with you to determine what and how you need the floor to perform.</p>
					<br/>
					<p style="margin-top: 0;">Step 2: Recommendation</p>
					<p style="margin-top: 0;">Based on our findings, we’ll put together a formal proposal that describes the issues that we discussed. The proposal will describe the manufacturer’s product, preparation and installation methods. Concare is a Master Contractor for many proven products. Due to our experience in installation and performance of the products, we’ll recommend which manufacturer’s product will perform and provide longevity with the best care. Our technicians are trained in the proper installation of the floor directly from each of the manufacturer’s.</p>
					<br/>
					<p style="margin-top: 0;">Step 3: Preparation</p>
					<p style="margin-top: 0;">Once awarded the contract, we begin preparing for the installation. Our website provides detailed descriptions on the various preparation techniques we use. Not all preparation methods are the same, and choosing the right method can make the difference between bonding and failing. Our technicians are trained on the various preparation methods and perform various checks to ensure accuracy. During this time, any repairs are noted and completed. It is important for our customers to know that Concare owns and maintains all of our own preparation equipment. This verifies a higher level of installation success.</p>
					<br/>
				    <p style="margin-top: 0;">Step 4: Installation</p>
					<p style="margin-top: 0;">Now that the preparation is complete, we’re ready to begin the installation of the new floor coating. As noted earlier, our technicians are trained on the proper installation techniques for the manufacturer’s we represent. View our full list of manufacturer’s now. Installing the new floor coating according to the manufacturer ensures proper adhesion and long-lasting wear.</p>
					<br/>
					<p style="margin-top: 0;">To view our video case studies and watch an installation, <a href="https://concare.com/food-beverage-concrete-floor-coating.php">click here now.</a></p>
		
