	        		<h1>High Performance Floor Protection for Specialty Peanut Room</h1>
	        		<p style="margin-top: 0; margin-bottom: 0;"><img src="images/nestleroasting.jpg" alt="floor protection under food processing area" width="321" height="240" hspace="5" vspace="5" align="right">Nestle Foods is dedicated to helping families improve their overall health and achieve their wellness goals by providing convenient, nutritious and delicious foods. With 25 manufacturing facilities, 24 distribution centers and nearly 25,000 employees in the United States alone, Nestle USA, with 2009 sales of 10.4 billion, is part of Nestle S.A. in Vevey, Switzerland the worlds largest food company, with 2009 sales of $99 billion.</p>
					<p>The company maintains a rigorous production schedule 24/7. The 3,800 sq. ft. peanut room processes and roasts peanuts to be placed in food products such as the Baby Ruth bar. The room has a continuous workflow, from preparing the raw peanuts to the roasting chamber and on to production. For food safety reasons such as the potential hazards of salmonella contamination, Nestle decided to divide the space into two separate areas, erecting a permanent barrier (a wall).</p>
					<p>Next, to maintain a sanitary environment, the company needed a cleanable work surface. The original surface contained pits and holes, perfect conditions for bacteria to thrive. During construction, Nestle also installed concrete curbing 4 feet high.</p>
					<p>Once the project was assessed and all of the goals considered, Concare prepared a comprehensive plan to remove the existing material from the floor and install a high build trowel finish on all of the curbs in order to create a highly cleanable environment.</p>
					<p>According to Nestle, Concare was awarded the contract due to previous successful installations, our ability to implement challenging projects on-time and on-budget, longevity of the floor and wall surfaces and quality workmanship. We agree.</p>
					<br>
					<h2>Timing and Scheduling of Personnel</h2>
					<p>Preparation in a 3,800 sq ft space with equipment that could not be moved, and in tight time frames, posed special challenges. To begin, Concare carefully selected a product that met the requirements and had the ability to withstand temperatures of 200-300 degrees F. Concare recommended and Nestle agreed to a cementitious polyurethane which can withstand high temperatures, meets USDA standards, and also has fewer steps in the installation process allowing for more preparation and curing time. Sloping the area to enable proper drainage when the walls and floors were being washed down was a late addition to the scope.</p>
					<p>Concare experienced significant time and logistical constraints during this project. Since this was a fully operational facility, timing was essential for Nestle. We worked together to identify blocks of time that would allow for the installation process. As a result, the project took place over 3 months with 4 separate weekend trips beginning in March of 2010. Our job would begin at 5:00 p.m. on Friday with preparation, installation of the new floor on Saturday with the last layer installed on Sunday morning with removal of all the protective coverings on Sunday evening. Nestle would come in early Monday for a wash down before resuming production. Our crews worked in shifts, around the clock, throughout the weekend.</p>
					<p>The project required a significant amount of set up. We approached the project by isolating the work area. We began with the raw peanut room as this was the most sensitive space due to the potential problem of contamination. Each time Concare left the space, we had to change clothes and place them into plastic containers so not to transport anything from the raw peanut area.</p>
					<p>The crew set up barriers to control dust, debris and any airborne particles. The workers used a high power vacuum to remove dust and debris as each section of the peanut room progressed. We placed all debris into plastic bags, sealed and transported to another part of the building for disposal. Plastic was securely fastened to each piece of the production equipment ensuring that no contaminants impacted their process.</p>
					<br>
					<h2>Quality Control</h2>
					<p style="margin-top: 0; margin-bottom: 0;"><img src="images/nestleroasting2.jpg" alt="floor protection under food processing area" width="321" height="240" hspace="5" vspace="5" align="right">Because of the complexities of the project, we performed core testing on the existing surface. We needed to understand if the slope was part of the original concrete or if it was added on top of it. Concare also needed to verify that the materials and thicknesses recommended were correct as well.</p>
					<p>Since there was the presence and build up of peanut oil, we needed to be sure the cementitious polyurethane would adhere to the floor. Concare performed a series of bond tests prior to application that predicted our chances of a successful installation.</p>
					<p>In addition, it was critical to test the integrity of the resloped floor as the installation was in progress. Inspections were made to ensure the absence of standing water, or birdbaths. Because sanitation is a vital issue, Concare tested and re-tested the elevation to ensure full drainage.</p>
					<br>
					<h2>Safety</h2>
					<p>Concare follows strict safety measures and Nestle was no exception. The Nestle facility required training for the specific site and we complied with all plant safety requirements. In addition, Concare has developed and regularly practices our own set of safety standards.</p>
					<p>For this specific peanut room project, Concare ensured all extension cords were tested and were properly grounded, contained no defects, cuts or expose wires. All personnel were PPE compliant, including the use of safety glasses, hard hats and steel toe boots. As a result of our efforts, the project was accident free.</p>
					<br>
					<h2>Value Analysis</h2>
