				<p style="margin-top: 0; margin-bottom: 0;"><img src="images/repairjointsconcrete.jpg" alt="repair joints in concrete floor" width="321" height="240" hspace="5" vspace="5" align="right"/><span>Joints are found in all concrete installations</span>, but there are several types, each of which requires a different remedy:</p>
				<p>1. Sawcut control joints are the very straight, one quarter inch wide, lines cut into brand new concrete installations, 2 inches deep or so, made with concrete saws. Spaced from 10 to 20 feet apart, they are created to direct concrete shrinkage cracks to a predetermined path. If left to its own devices, concrete will crack in a random pattern, but these sawcut control joints will usually have that crack running through them (just clean one out and take a look). After a few months, these are no longer needed and are usually filled to eliminated chipping along the sawcut.</p>
				<p>2. Cold joints can look just like sawcut control joints, since they are also very straight, but you will almost always find cold joints running along the column lines. The gap is not one quarter inch, like sawcut control joints, but since they are not cut at all, more like a hairline width. Cold joints represent the places where the concrete installation team stopped work each day. These joints are more susceptible to widening over time, since the concrete slab is often fully independent from one side to another.</p>
				<p><img src="images/repairjointsconcrete2.jpg" alt="after repair joints in concrete floor" width="321" height="240" hspace="5" vspace="5" align="right"/>3. <span>Expansion joints</span>. These are engineered into a building to allow for the normal expansion and contraction which occurs as a result of seasonal heating and cooling, or between structures that have independent foundations or to accommodate flexing and vibration. Identifying and treating expansion joints is critical to maintain the structural integrity of the building itself, especially when protective toppings or coatings are applied.</p>
				<p>4. Isolation joints are designed into a structure to keep areas separated. The most common isolation joint you will find is around the columns supporting the roof, the so-called "diamonds". These are specifically designed to keep the effect of heavy snow loads, for example, from cracking or damaging the concrete floor slab. You will also find isolation joints around foundations that support heavy production equipment, since those foundations can be several times the thickness of the concrete floor and equipment vibration can be transferred into the floor, also causing damage.</p>
