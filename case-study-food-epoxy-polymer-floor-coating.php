	        		<p style="margin-top: 0; margin-bottom: 0;"><img src="images/popcorn1.jpg" alt="failing floor system to food grade high performance floor" width="421" height="340" hspace="5" vspace="5" align="right">Double Good is a flavored gourmet popcorn company consistently growing in popularity over the past 15 years, due in no small part to its commitment to hand batch production methods. The popcorn is sold primarily online through its e-commerce store and school fundraisers. The continued growth has allowed the privately held company to purchase a building in Burr Ridge, IL. The 45,000 ft. facility needed approximately half to be renovated for the manufacturing, preparation and packaging of their specialty popcorn.</p>
					<p>Concare was invited to evaluate the condition of the concrete and recommend a solution for sanitation, safety, cleanability and more. The news was not quite what Double Good was expecting.</p>
					<br>
					<h2>The Evaluation</h2>
					<p>Concare discovered that the existing  concrete floor was scaling, in fact had been scaling for years. Scaling is a concrete condition exhibiting 1/8 inch flaking at the surface. This is caused by wetting of the surface during finishing, or excess air entrainment in the concrete ready-mix. Occasionally, chemical exposures early in the concrete curing cycle will lead to this condition. Regardless of source, the top portion of the concrete was weak.</p>
					<p>In the case of Double Good, the scaling condition had progressed to exposed aggregate, dusting & an uneven floor surface; previous floor patches were failing as well. Concare presented its findings to the owner who was committed to fixing this condition, but was not sure how to best accomplish that.</p>
					<p>Concare set up 4 test areas in the facility with possible solutions in order to understand which flooring preparation and product would best resolve the scaling and deliver a safe, food grade, durable work surface.</p>
					<p>Option 1: Remove the scaling with scarification and diamond polish & densify the surface leaving  the aggregate exposed</p>
					<p>Option 2: Remove the scaling and apply a thin coating with a fine quartz broadcast for enhanced traction</p>
					<p>Option 3: Remove the scaling and install a coating with a medium grade quartz for better hide & durability </p>
					<p>Option 4: Remove the scaling and install a 1/4" thick polymer resurfacer, sealed and finished.</p>
					<br>
					<p>The test results revealed:</p>
					<p>Option 1: The test revealed too many pin holes and voids and would cause sanitation issues immediately, plus the surface was too smooth.</p>
					<p>Option 2 & 3: These applications would not provide a uniform finish & less durable.</p>
					<p>Option 4: Restored the integrity of the flooring surface and eliminated any pin holes and voids while delivering the longest life.</p>
					<p>Concare still wasn't done with the evaluation. Next, we needed to take into consideration the hot oil from popping kettles and the finishing operations where flavors & chocolate are added, creating a sticky and oily residue. Double Good also wanted to install a high pressure hot water cleaning system in the main production area to clean and sanitize the equipment.</p>
					<br>
					<h2>The Choice-s</h2>
					<p style="margin-top: 0; margin-bottom: 0;"><img src="images/popcorn2.jpg" alt="epoxy polymer flooring" width="421" height="340" hspace="5" vspace="5" align="right">Concare recommended two different flooring applications for the production area. In the main production section Concare installed a cementitious polyurethane that can withstand the high temperatures of steam cleaning as well as hot oil spills. In the warehousing, packaging, and shipping area, an epoxy polymer resurfacer was used to level out the floor to create a uniform surface.</p>
					<p>Regardless of solution, the facility is required to adhere to sanitation standards. Concare installed an integral cove base on the wall to eliminate any residues that may accumulate in the corner.</p>
					<br>
					<h2>Preparation & Installation</h2>
					<p>The surface preparation was quite extensive, requiring specialized machinery and techniques for milling, scarification and abrasive blast cleaning. This ensured a surface free of the scaling condition, with no loose residue left behind.</p>
					<p>Concare was faced with removal of previous floor coating layers that required additional preparation in order to get to the substrate. Our state-of-the-art milling equipment gave Concare the ability to remove anywhere from 1/8 inch to 3/16 inch from the surface of the floor prior to applying the polymer topping.</p>
					<br>
					<h2>Multi-Tasking Floor Types</h2>
					<p>As Concare worked on the two flooring solutions in the production area, the installation team worked on two additional areas in the facility.</p>
					<p>Reception: The reception area and offices were covered with carpet and tile and Double Good wanted a more contemporary look. Concrete polishing (Option #1 from the test) proved the right choice, with the addition of color stain. Concare removed the tile mastics and contaminants from the highly visible area. We ground and polished the floor and installed a clear protective surface coating that would be easy to clean and stain resistant.</p>
					<p>Restrooms: Providing a place for workers to wash up in a clean and sanitary environment was important to Double Good. Concare installed an MMA full flake floor which was the ideal solution for sanitation, cleanability, aestetics and safety. Double Good wanted to ensure the texture installed on the floor could withstand wet conditions in order to prevent employees and visitors from slipping and falling, yet be relatively easy to keep clean.  The MMA choice captured these requirements, and beautifully!</p>
