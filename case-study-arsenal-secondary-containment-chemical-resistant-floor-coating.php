	        		<p style="margin-top: 0; margin-bottom: 0;"><img src="images/arsenal1.jpg" alt="equipment base floor protection" width="321" height="240" hspace="5" vspace="5" align="right">The weapons manufacturing company is the largest government-owned weapons manufacturing arsenal in the U.S. It's been an active manufacturer of military equipment since the 1880's. Maintaining 130+ year old buildings is an ongoing challenge, particularly where modern chemical use impacts these older structures. Compounding matters is the close proximity to the Mississippi River, into which even the tiniest chemical spill cannot be allowed.</p>
					<p>The 26,300 sq.ft. scope of project consisted of 4 containment areas directly below the metal plating and finishing operation. Harsh acids, including chromic & nitric, overflow from chemical dip tanks and are captured within the containments.</p>
					<p>During the evaluation, an existing floor topping material was identified, tested, examined and determined to be fit for renovation. As a result, Concare recommended a vinyl ester technology for its high chemical resistance and the Arsenal required fire retardant properties, necessitating a custom solution. The vinyl ester solution was also fiberglass reinforced to prohibit cracking.</p>
					<p>Concare also identified a potential roadblock: getting large preparation equipment into the basement and over the curbing surrounding the containment areas.  With a little imagination and practical engineering, the Concare team built a ramping system to overcome the obstacle and be sure that the critical preparation could be done with the right equipment.</p>
					<br>
					<h2>The Art of the Floor</h2>
					<p>Installing a floor protection system so critical required extensive pre-planning and preparation. First were the security clearances to conform to the Homeland Security directives. Then a detailed Project Specific Safety Plan was created, reviewed, modified and approved by the HSA manager. Required compliance with Contracting Manpower Data Elements had to be approved. Since the Concare installation team had only 6 days to complete each containment area, logistical challenges and a by-the-hour schedule had to be identified and approved by the production department.  There were a total of 8 Concare employees plus their lead man, the Senior Project Manager, and two representatives from Protective Industrial Polymers who had to be integrated fully into the plan, both for execution and for quality control oversight.  Each phase was meticulously planned leaving as little room for error as possible. </p>
					<p style="margin-top: 0; margin-bottom: 0;"><img src="images/arsenal2.jpg" alt="fiberglass chopped strand in vinyl ester floor solution" width="321" height="240" hspace="5" vspace="5" align="right">As the project unfolded, the installation team began each phase with a controlled surface grinding, precisely done to preserve the surface without damaging the existing coating. Abrasive shot blasting with high powered vacuums was used to remove all debris, contaminants and particulate to create the conditions required for adhesion.  Next, the sump pits located within each containment area, had to be cleaned and hand prepped to the same level of detail, so that a paste-like formulation of the vinyl ester resin could be installed.</p>
					<p>Once all preparation was complete, the Concare team measured and sized 3 sets of fiberglass: two sets of 1.5 ounce chopped strand and one set of 1 ounce veil, which is the top layer.  The team trowelled the vinyl ester resin mixture on floor and up the berms, laying in the first layer of fiberglass.  Immediately after that was completed, another layer of resin was applied along with the second layer of fiberglass. A layer of resin and the fiberglass veil along with a final coating of resin completed the installation. This process was repeated each of the four phases.</p>
