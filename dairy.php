	        		<h1>Dairy Floor Protection</h1>
					<br>
					<br>
	        		<p><b>Two dairy companies. Two positive results for listeria. Two different approaches. Which one will work for the long term?</b> </p>
					<p>Interview Dairy #1</p>
					<p>Q: How did you know you had a listeria problem? </p>
					<p><b>A1: We regularly swab test the floor, and it continues to progress.</b> </p>
					<p>Q: What type of floor do you have? </p>
					<p><b>A1: We have a dairy brick floor.</b> </p>
					<p>Q: What have you done to try to control the listeria problem? </p>
					<p><b>A1: It seems everything. We’ve cleaned the floor with chemicals, we’ve even flooded the floor with sanitation chemicals in the hopes of controlling the growth of listeria and other bacteria.</b> </p>
					<p>Q: The result? </p>
					<p><b>A1: Mixed results. Even though we had a reduction of bacteria and listeria growth, it does come back relatively quickly. We also found that when we flooded it, the listeria went down through the grout and cracks around the dairy bricks and continued its growth underneath. We realized that dairy bricks move or shift. We may not visibly ‘see’ the bricks moving, or ‘feel’ it as we walk, but all bacteria and listeria needs is a hairline crack or void in the floor. </b> </p>
					<p>Q: Were you ever able to fix the problem? </p>
					<p><b>A1: No. We ended up selling the building.</b> </p>
					<br>
					<p>Interview Dairy #2</p>
					<p>Q: Your company decided to take on the listeria problem head on. </p>
					<p><b>A2: Yes, we were experiencing a chronic problem with listeria. We couldn’t afford to have our ice cream recalled. We had to do something.</b> </p>
					<p>Q: What approach did you take? </p>
					<p><b>A2: We brought in several flooring companies that claim they understood our problem. We had someone who said that a ¼” floor coating on top of the dairy brick would do the trick. Well, true if you only want a floor solution to last only a couple years. One crack, and we’re back to where we started.</b> </p>
					<p>After meeting with many concrete floor coating contractors, we decided to take the advice of Concare. We tore out all of the dairy brick, and went down to the bare concrete. Concare installed a pre-treatment that reacts within the slab to form an antimicrobial gel that fills the pores of the concrete.  The heavy duty cementitious urethane wear surface over that also includes  antimicrobials.  Even if we get a crack or damage to the topping, we are still protected. Then another layer goes on top of the concrete, which protects the concrete as well as prevents the growth of microbes. </p>
					<p>Q: How long have you had the solution in place?  </p>
					<p><b>A2: Well the oldest part of the floor is 3 years old.</b> </p>
					<p>Q: Can you elaborate? </p>
					<p><b>A2: We took the approach of breaking our 60,000 sq ft facility into 4 parts. Each year we did a section. It was better for the company financially to endure, but more importantly, we don’t have to go back to those sections and re-do the floors in 3-5 years because the solution didn’t hold up. </b> </p>
					<p>Q: How did you come up with that idea? </p>
					<p><b>A2: We spoke to a couple of companies in and outside of our industry, and their approach made the most sense. One of the companies did more than a floor, they renovated electricity, plumbing etc., a section at a time. It was a smart approach, and one that we were able to bring to executive management as a long-term viable solution.</b> </p>
